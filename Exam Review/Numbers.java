public class Numbers {
    public static void main(String[] args) {
        for (int j=6; j>=0; j--) {
            for (int q=0; q<j-2; q++) {
                int w=j-2;
                for (int y=0; y<w; y++) {
                    System.out.print(j);
                    
                }
                System.out.println();
                
            }
            w--;
        }
}

}
